CREATE TABLE VAR_FOCOS_INPE AS

SELECT 
 	 DT_FOCO
	,ESTADO
	,MUNICIPIO
	,BIOMA
	,LATITUDE
	,LONGITUDE
	,1 AS FOCO_REGISTRADO
FROM FOCOS_INPE
GROUP BY 
 	 DT_FOCO
	,ESTADO
	,MUNICIPIO
	,BIOMA
	,LATITUDE
	,LONGITUDE;

CREATE INDEX index_VAR_FOCOS_INPE
ON VAR_FOCOS_INPE
	(
	DT_FOCO,
	ESTADO,
	MUNICIPIO,
	BIOMA
	);